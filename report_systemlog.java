    private void searchBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTActionPerformed
        String monthFilter = monthTF.getText();
        String yearFilter = yearTF.getText();
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        FileReader file;
        model.setRowCount(0);
        try {
            file = new FileReader("systemlog.txt");
            BufferedReader reader = new BufferedReader(file);
            Object[] tableLines = reader.lines().toArray();
            for (int i = 0; i < tableLines.length; i++) {
                String line = tableLines[i].toString().trim();
                String[] info = line.split(",");
                String[] logindate = info[2].split("-");
                if (logindate[1].equals(monthFilter) && logindate[0].equals(yearFilter)) {
                    model.addRow(info);
                } else if (logindate[1].equals(monthFilter) && yearFilter.isEmpty()) {
                    model.addRow(info);
                } else if (logindate[0].equals(yearFilter) && monthFilter.isEmpty()) {
                    model.addRow(info);
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(report_systemlog.class.getName()).log(Level.SEVERE, null, ex);
        }
        totalCount();
    }//GEN-LAST:event_searchBTActionPerformed

