    public void displayTable() {
        try {
            FileReader file;
            file = new FileReader("systemlog.txt");
            BufferedReader reader = new BufferedReader(file);
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            Object[] tableLines = reader.lines().toArray();
            for (int i = 0; i < tableLines.length; i++) {
                String line = tableLines[i].toString().trim();
                String[] info = line.split(",");
                model.addRow(info);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(report_systemlog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void totalCount() {
        int rowsNum = table.getRowCount();
        String rows = Integer.toString(rowsNum);
        total.setText(rows);
    }

