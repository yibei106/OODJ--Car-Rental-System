    public void getTotal() {
        double sum = 0;
        for (int j = 0; j < table.getRowCount(); j++) {
            sum = sum + Double.parseDouble(table.getValueAt(j, 6).toString());
        }
        String totalPrice = String.valueOf(sum);
        total.setText(totalPrice);
    }

    public void displayTable() {
        FileReader file;
        try {
            file = new FileReader("paymenthistory.txt");
            BufferedReader reader = new BufferedReader(file);
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            Object[] tableLines = reader.lines().toArray();
            for (int i = 0; i < tableLines.length; i++) {
                String line = tableLines[i].toString().trim();
                String[] info = line.split(",");
                String paymentInfo = info[9] + "," + info[10] + "," + info[0] + "," + info[1] + "," + info[2] + "," + info[4] + "," + info[5] + "," + info[3];
                String[] tableData = paymentInfo.split(",");
                model.addRow(tableData);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(car_info.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

