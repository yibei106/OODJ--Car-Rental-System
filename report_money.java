    private void searchBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTActionPerformed
        // TODO add your handling code here:
        String monthFilter = monthTF.getText();
        String yearFilter = yearTF.getText();
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        FileReader file;
        model.setRowCount(0);
        try {
            file = new FileReader("paymenthistory.txt");
            BufferedReader reader = new BufferedReader(file);
            Object[] tableLines = reader.lines().toArray();
            for (int i = 0; i < tableLines.length; i++) {
                String line = tableLines[i].toString().trim();
                String[] info = line.split(",");
                String[] paiddate = info[9].split("-");
                if (paiddate[0].equals(yearFilter) && paiddate[1].equals(monthFilter)) {
                    String paymentInfo = info[9] + "," + info[10] + "," + info[0] + "," + info[1] + "," + info[2] + "," + info[4] + "," + info[5] + "," + info[3];
                    String[] tableData = paymentInfo.split(",");
                    model.addRow(tableData);
                } else if (monthFilter.isEmpty() && paiddate[0].equals(yearFilter)) {
                    String paymentInfo = info[9] + "," + info[10] + "," + info[0] + "," + info[1] + "," + info[2] + "," + info[4] + "," + info[5] + "," + info[3];
                    String[] tableData = paymentInfo.split(",");
                    model.addRow(tableData);
                } else if (yearFilter.isEmpty() && paiddate[1].equals(monthFilter)) {
                    String paymentInfo = info[9] + "," + info[10] + "," + info[0] + "," + info[1] + "," + info[2] + "," + info[4] + "," + info[5] + "," + info[3];
                    String[] tableData = paymentInfo.split(",");
                    model.addRow(tableData);
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(car_info.class.getName()).log(Level.SEVERE, null, ex);
        }
        getTotal();


    }//GEN-LAST:event_searchBTActionPerformed

